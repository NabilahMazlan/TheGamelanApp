package com.example.gamelannagakencana;

public class TypesClass {
    private String imgView;
    private String txt;

    public TypesClass() {
    }

    public TypesClass(String imgView, String txt) {
        this.imgView = imgView;
        this.txt = txt;
    }

    public String getImgView() {
        return imgView;
    }

    public void setImgView(String imgView) {
        this.imgView = imgView;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }
}
